package edu.csuft.fumenhqian.com;

public class Film {
	
	/**
	 * 名称
	 */
	String titile;
	
	/**
	 * 相关信息
	 */
	String info;
	
	/**
	 * 评分
	 */
	double rating;
	
	/**
	 * 评分人数
	 */
	int num;
	
	/**
	 * 排名
	 */
	int id;
	
	/**
	 * 海报
	 */
	String poster;
	
	/**
	 * 短评
	 */
	String quote;
	
	String dir;

	@Override
	public String toString() {
		return "Film [titile=" + titile + ", info=" + info + ", rating=" + rating + ", num=" + num + ", id=" + id
				+ ", poster=" + poster + ", quote=" + quote + ", dir=" + dir + "]";
	}

	}
	
	
	
	
	
	
	
	


